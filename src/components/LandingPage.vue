        { text: "O Demander transformou a forma como gerenciamos nossas vendas. A eficiência aumentou exponencialmente.", author: "João Silva" },
        { text: "Uma ferramenta essencial para qualquer negócio que deseja crescer e se destacar no mercado.", author: "Maria Oliveira" },
        { text: "O suporte ao cliente é excepcional e sempre pronto para ajudar nas nossas necessidades.", author: "Pedro Santos" },
        { text: "Recomendo fortemente o Demander para qualquer empresa que deseja melhorar sua força de vendas.", author: "Ana Costa" },
        { text: "Desde que começamos a usar o Demander, nossas vendas aumentaram significativamente.", author: "Lucas Ferreira" },
        { text: "A integração com nossa plataforma de e-commerce foi perfeita e sem complicações.", author: "Gabriela Souza" },
        { text: "Uma solução completa e fácil de usar que atende todas as nossas necessidades.", author: "Ricardo Lima" },
        { text: "O Demander é indispensável para o sucesso da nossa equipe de vendas.", author: "Camila Rocha" }
      ],
      currentGroup: 0,
      displayedDepoimentos: [],
      isActive: true,
      isButtonVisible: false
    };
  },
  mounted() {
    const sections = [this.$refs.section1, this.$refs.section2, this.$refs.section3, this.$refs.section4, this.$refs.section5, this.$refs.section6, this.$refs.section7, this.$refs.section8, this.$refs.section9, this.$refs.section10];

    const observer = new IntersectionObserver((entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add('fadeInUp-animation');
          entry.target.classList.remove('opacity-0');
          observer.unobserve(entry.target);
        }
      });
    }, {
      threshold: 0.1,
    });

    sections.forEach((section) => {
      observer.observe(section);
    });

    this.startCardRotation();

    const backToTopButton = document.getElementById('back-to-top');
    if (backToTopButton) {
      backToTopButton.addEventListener('click', () => {
        window.scrollTo({ top: 0, behavior: 'smooth' });
      });
    }
    window.addEventListener('scroll', this.handleScroll);
  },
  methods: {
    startCardRotation() {
      const groupSize = 4;
      const totalGroups = Math.ceil(this.depoimentos.length / groupSize);

      const showNextGroup = () => {
        this.isActive = false;

        setTimeout(() => {
          this.displayedDepoimentos = this.depoimentos.slice(
            this.currentGroup * groupSize,
            (this.currentGroup + 1) * groupSize
          );

          this.isActive = true;
          this.currentGroup = (this.currentGroup + 1) % totalGroups;
        }, 1000);
      };

      showNextGroup();
      setInterval(showNextGroup, 5000);
    },
    handleScroll() {
      this.isButtonVisible = window.scrollY > 300;
    }
  }
};
</script>
